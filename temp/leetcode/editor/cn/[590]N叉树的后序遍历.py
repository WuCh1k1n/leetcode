class Solution(object):
    def postorder(self, root):
        """
        :type root: Node
        :rtype: List[int]
        """
        if not root:
            return []
        traversal = []
        for child in root.children:
            traversal.extend(self.postorder(child))
        traversal.append(root.val)
        return traversal
