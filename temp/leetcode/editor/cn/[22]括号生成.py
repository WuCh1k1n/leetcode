class Solution:
    def generateParenthesis(self, n: int) -> List[str]:
        def backtrack(path: str, pairs: int, left: int, right: int):
            if left == right == pairs:
                res.append(path)
                return
            if left < pairs:
                backtrack(path + '(', pairs, left + 1, right)
            if right < left:
                backtrack(path + ')', pairs, left, right + 1)

        res = []
        backtrack('', n, 0, 0)
        return res
