class Solution(object):
    def letterCombinations(self, digits):
        """
        :type digits: str
        :rtype: List[str]
        """
        if not digits:
            return list()

        phone_map = {
            "2": "abc",
            "3": "def",
            "4": "ghi",
            "5": "jkl",
            "6": "mno",
            "7": "pqrs",
            "8": "tuv",
            "9": "wxyz",
        }

        def backtrack(index):
            if index == len(digits):
                ans.append("".join(combination))
            else:
                for letter in phone_map[digits[index]]:
                    combination.append(letter)
                    backtrack(index + 1)
                    combination.pop()

        combination = list()
        ans = list()
        backtrack(0)
        return ans
